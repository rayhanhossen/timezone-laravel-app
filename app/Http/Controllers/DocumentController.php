    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {


        $document1 = time().'.'.$request->document1->extension();

        $request->document1->move(public_path('documents'), $document1);

        $document2 = time().'.'.$request->document2->extension();

        $request->document2->move(public_path('documents'), $document2);

        $request->merge(['document1' => $document1]);
        $request->merge(['document2' => $document2]);





        Document::create([
            'title'=>$request['title'],
            'document1'=>$document1,
            'document2'=>$document2,
        ]);

        return redirect()->route('document.index');


    }

